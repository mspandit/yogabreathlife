	public static function get_recipients($query,$mail_id,$mail_main_id = false)
	{
		global $wpdb;
		$users 	= $wpdb->get_results( $query );

		if ($users)
		{
			$replacements  	= array ();
			foreach($users as $user) 
			{
				$replacements [$user->email] 	= array ( 	'{{toemail}}'       => $user->email ,
											'{{mp_confkey}}'	  => $user->confkey,
                                                            	'{{unsubscribe}}'   => self::get_unsubscribe_url($user->confkey) ,
	                                                            '{{viewhtml}}'      => self::get_view_url($user->confkey,$mail_id)
	                                                    );
				$replacements [$user->email] 	= apply_filters('MailPress_get_recipients',$replacements [$user->email],$user->id,$mail_main_id);
			}
			return $replacements;
		}
		else return array();
	}

	public static function get_meta_recipients($replacements,$user_id,$mail_main_id) 
	{
		$metas = MP_Usermeta::get( $user_id );
		
		if (!is_array($metas)) $metas = array($metas);

		foreach ($metas as $meta)
		{
			if ($meta->meta_key[0] == '_') continue;

			$replacements['{{' . $meta->meta_key. '}}'] = $meta->meta_value;
		}
		return $replacements;
	}


